						<div class="row">
							<article class="col-4 col-12-mobile special">
								<a href="#" class="image featured"><img src="images/etang_de_lor.jpg" alt="" /></a>
								<header>
									<h3><a href="#">L'étang de l'Or</a></h3>
								</header>
								<p>
								A la lisière de la Ville, l’Etang de l’Or dévoile un patrimoine naturel exceptionnel. Classé Natura 2000 depuis 2007, l’Etang de l’Or est riche de paysages multiples : d’un côté les plaines agricoles et de l’autre les marais. Chaque année, c’est là que des milliers d’oiseaux migrateurs ou sédentaires hibernent et se reproduisent. Pour accéder à ce site sauvage et préservé, vous pouvez emprunter l’ancien chemin de halage de la Capoulière et poursuivre la balade sur un sentier balisé de 5 km. Le chemin du cabanier, un incontournable, pour les amoureux des balades naturalistes. A protéger absolument !
								</p>
							</article>
							<article class="col-4 col-12-mobile special">
								<a href="#" class="image featured"><img src="images/arenes.jpg" alt="" /></a>
								<header>
									<h3><a href="#">Les Arènes</a></h3>
								</header>
								<p>
								Situées au centre de Mauguio, les arènes sont l’écrin de nos traditions.
								Construite en 1989, ces arènes en "béton" ont définitivement cellé notre attachement à la culture camarguaise.
								On peut retrouver dans nos archives ce qui fût d'antan les prémices des arènes. En effet, sur la place de la Mairie actuelle, des arènes de fortune étaient déjà le théâtre de jeux avec le taureau et des premiers razets. Quelques charrettes positionnées en rond faisaient office de barrières et l'on se donnait rendez-vous sur le "plan" pour assister à ce spectacle.
								Les premières arènes avec gradins - en bois à l'époque - ont été installées en 1926, Boulevard Jean Macé. Revisitées et consolidées en 1989, elles peuvent accueillir plus de  2000 personnes. 
								C'est au coeur de ces arènes que se partage avec émotion et intensité la culture camarguaise avec notamment la Temporada (saison taurine) et la Fête votive, sans aucun doute le point d'orgue de la saison.
								</p>
							</article>
							<article class="col-4 col-12-mobile special">
								<a href="#" class="image featured"><img src="images/theatre.jpg" alt="" /></a>
								<header>
									<h3><a href="#">Le théâtre Bassaget</a></h3>
								</header>
								<p>
								Il porte le nom de son créateur : Samuel Bassaget. Maire de la Ville de 1930 à 1941, il a fait construire en 1933, ce qui fût longtemps le Foyer Bassaget. Influencé par de grands architectes comme Le Corbusier, Samuel Bassaget a voulu réaliser un bâtiment en béton armé à ossature apparente. C’est d’ailleurs l’un des rares édifices de ce type dans la région. A l’origine, le théâtre était une salle de cinéma. D'ailleurs, vous pouvez deviner, sur la façade du Foyer, la cabine de projection décorée de bas-reliefs.
								Depuis 2003, le Foyer est officiellement devenu le Théâtre municipal : la salle de spectacle de la Ville de Mauguio Carnon. Il est le lieu privilégié de la programmation culturelle - théâtre, concert, danse - des ateliers thématiques et des représentations organisées par les associations. Grâce à ce lieu, la Ville déploie une offre culturelle riche et accessible à tous. Un engagement renforcé par des actions d’éducation artistique et le soutien à la création avec des compagnies en résidence.
								</p>
